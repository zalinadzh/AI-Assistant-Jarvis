def ask_openrouter(user_input):
    url = "https://openrouter.ai/api/v1/chat/completions"
    
    # Запоминаем вопрос пользователя
    chat_history.append({"role": "user", "content": user_input})
    
    headers = {
        "Authorization": f"Bearer {OPENROUTER_API_KEY}",
        "Content-Type": "application/json"
    }
    
    # Здесь мы выбираем модель. Например, универсальную и дешевую
    data = {
        "model": "google/gemini-2.0-flash-lite-001", # Можно менять на другие модели
        "messages": chat_history
    }
    
    try:
        # Добавляем цикл на 3 попытки прямо здесь
        for attempt in range(3):
            response = requests.post(url, headers=headers, data=json.dumps(data))
            
            # Если всё прошло успешно (код 200)
            if response.status_code == 200:
                result = response.json()
                answer = result['choices'][0]['message']['content']
                # Запоминаем ответ в память
                chat_history.append({"role": "assistant", "content": answer})
                return answer
            
            # Если сервер занят (429) или глючит (500+), ждем 2 секунды и идем на новый круг
            elif response.status_code in [429, 500, 502, 503, 504]:
                print(f"Сервер капризничает, попытка {attempt + 1}...")
                import time
                time.sleep(2)
            else:
                return f"Ошибка: {response.status_code}"
                
    except Exception as e:
        return f"Критическая ошибка: {e}"

